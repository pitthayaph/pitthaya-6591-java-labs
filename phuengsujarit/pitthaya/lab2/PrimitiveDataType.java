 package phuengsujarit.pitthaya.lab2;
public class PrimitiveDataType {
   
    public static void main(String[] args) {
        String studentid= "663040659-1";
        String firstname = "Pitthaya";
        byte bytevalue = 91;
        Short shortvalue = 591;
        Integer intvalue = 406591;
        Long longvalue =  (long) 30406591;
        Float floatvalue = (float) 0.591;
        Double doublevalue = 0.30406591;
        Character charvalue = 'P';
        Boolean booleanvalue  = true;

        System.out.println("Student ID : " + studentid);
        System.out.println("Firstname : "+ firstname);
        System.out.println("Byte value : " + bytevalue);
        System.out.println("Shortvalue : "+ shortvalue);
        System.out.println("Int value : " + intvalue);
        System.out.println("Long value : " + longvalue);
        System.out.println("Float value : " + floatvalue);
        System.out.println("Double value : " + doublevalue);
        System.out.println("Char value : " +charvalue);
        System.out.println("Char value : " +booleanvalue);

        
    







    }
}
